def get_post_type(hour: int) -> str:
    if hour <= 9:
        return "freelance_howto"
    elif 10 <= hour <= 13:
        return "freelance_ai"
    elif 14 <= hour <= 17:
        return "job_type_idea"
    elif hour == 18 or hour == 19:
        return "ai_beginner"
    elif hour == 20 or hour == 21:
        return "ai_case_study"
    else:
        return "business_strategy"


def get_topic_by_rotation(post_type: str, hour: int, offset: int = 0) -> str:
    """
    日付とオフセットに基づいてトピックをローテーション選択する。
    random.choiceではなくtoordinal()を使うことで再現性を確保し、
    同じ日・同じ時間帯では必ず同じトピックになる。
    offsetを使うと再生成時に別のトピックを取得できる。
    """
    topics = POST_TOPICS[post_type]["topics"]
    today = datetime.now(JST)
    index = (today.toordinal() + hour + offset) % len(topics)
    return topics[index]


